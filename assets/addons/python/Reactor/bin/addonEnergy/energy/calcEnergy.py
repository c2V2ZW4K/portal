import math

def megatons_to_joules():
    megatons = float(input("Enter the amount of megatons: "))

    if not math.isnan(megatons):
        joules = megatons * 4.184 * math.pow(10, 15)
        return_mass = input("Do you want to calculate the mass? (y/n): ")

        if return_mass.lower() == "y":
            mass = joules / (8.988 * math.pow(10, 13))
            mass_in_tonnes = mass / 1000
            print(f"The mass required to generate {joules} joules is {mass:.2f} kg")
            print(f"The mass required to generate {joules} joules is {mass_in_tonnes:.2f} tonnes")
        else:
            print(f"{megatons} megatons is equal to {joules:.2f} joules")
    else:
        print("Invalid input for megatons.")

def joules_to_megatons():
    joules = float(input("Enter the amount of joules: "))

    if not math.isnan(joules):
        megatons = joules / (4.184 * math.pow(10, 15))
        return_mass = input("Do you want to calculate the mass? (y/n): ")

        if return_mass.lower() == "y":
            mass = joules / (8.988 * math.pow(10, 13))
            mass_in_tonnes = mass / 1000
            print(f"The mass required to generate {joules} joules is {mass:.2f} kg")
            print(f"The mass required to generate {joules} joules is {mass_in_tonnes:.2f} tonnes")
        else:
            print(f"{joules} joules is equal to {megatons:.2f} megatons")
    else:
        print("Invalid input for joules.")

def convert_mass():
    uranium_mass = float(input("Enter the uranium mass in kg: "))

    if not math.isnan(uranium_mass):
        joules = uranium_mass * 8.988 * math.pow(10, 13)
        megatons = joules / (4.184 * math.pow(10, 15))
        print(f"The amount of energy generated by {uranium_mass} kg of uranium is {joules:.2f} joules")
        print(f"The amount of energy generated by {uranium_mass} kg of uranium is {megatons:.2f} megatons")
    else:
        print("Invalid input for uranium mass.")

def main():
    while True:
        print("Choose an option:")
        print("1. Convert megatons to joules")
        print("2. Convert joules to megatons")
        print("3. Convert uranium mass to joules and megatons")
        print("4. Quit")

        option = int(input("Selected option: "))

        if option == 1:
            megatons_to_joules()
        elif option == 2:
            joules_to_megatons()
        elif option == 3:
            convert_mass()
        elif option == 4:
            print("Exiting the program...")
            break
        else:
            print("Invalid option.")

main()

